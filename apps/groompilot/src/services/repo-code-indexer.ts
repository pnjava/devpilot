function isTestFilePath(file: string): boolean {
  const lower = file.toLowerCase();
  return (
    lower.includes("/src/test/") ||
    lower.includes("/tests/") ||
    lower.includes("/test/") ||
    /(test|spec|it)\.[a-z0-9]+$/.test(lower)
  );
}

function classifySubsystem(file: string): string {
  const path = file.toLowerCase();
  if (/auth|security|jwt|oauth|login|token/.test(path)) return "auth";
  if (/payment|transaction|ledger|settlement|invoice|refund|billing|reconciliation|groom/.test(path)) return "payments";
  if (/customer|profile|user|email|notification/.test(path)) return "customer";
  if (/db|migration|schema|sql|liquibase|flyway/.test(path)) return "data";
  if (/metrics|prometheus|telemetry|observability|logging/.test(path)) return "observability";
  if (/docker|helm|deploy|infra|k8s|terraform|jenkins|workflow/.test(path)) return "platform";
  if (/api|route|controller|openapi|swagger/.test(path)) return "api";
  if (/frontend|component|tsx$|jsx$|css$/.test(path)) return "frontend";
  if (/test|spec/.test(path)) return "test";
  return "core";
}

function detectSensitivity(filePath: string, content: string): string[] {
  const path = filePath.toLowerCase();
  const text = content.toLowerCase();
  const sensitivity: string[] = [];
  if (/auth|security|jwt|oauth|token|login|rbac|permission/.test(path) || /authorize|permission|role|token|jwt/.test(text)) sensitivity.push("auth");
  if (/payment|transaction|refund|ledger|settle|reconcil|money|balance/.test(path) || /transfer|charge|debit|credit|balance/.test(text)) sensitivity.push("money-movement");
  if (/pii|customer|email|profile|user|personal|ssn|pan|card/.test(path) || /email|ssn|pan|customer|personal/.test(text)) sensitivity.push("pii");
  if (/crypto|encrypt|decrypt|cipher|signature|hash/.test(path) || /messagedigest|cipher|getinstance|sha1|md5|des|rsa/.test(text)) sensitivity.push("crypto");
  if (/migration|schema|sql|flyway|liquibase/.test(path)) sensitivity.push("migrations");
  return sensitivity;
}

