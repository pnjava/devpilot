function extractAnnotations(content: string, language: string, symbols: ExtractedSymbol[]): ExtractedAnnotation[] {
  if (language !== "java" && language !== "groovy") return [];

  const annotations: ExtractedAnnotation[] = [];
  const lines = content.split("\n");
  const annotationPattern = /^\s*(@[A-Za-z]\w*)(?:\(([^)]*)\))?/;

  for (let i = 0; i < lines.length; i++) {
    const match = lines[i].match(annotationPattern);
    if (!match) continue;

    const annotName = match[1];
    const params = match[2] || undefined;
    const classification = ANNOTATION_CLASSIFICATIONS[annotName.toLowerCase()] || "other";

    // Find nearest following symbol
    let targetSymbol = "module";
    for (const sym of symbols) {
      if (sym.lineStart > i + 1) {
        targetSymbol = sym.name;
        break;
      }
    }

    annotations.push({
      targetSymbol,
      annotationName: annotName,
      annotationParams: params,
      classification,
      lineNumber: i + 1,
    });
  }

  return annotations;
}

// ─── Guard detection ───────────────────────────────────────────────────────────

interface ExtractedGuard {
  protectingSymbol?: string;
  guardType: string;
  scope?: string;
  evidence: string;
  lineNumber: number;
}

function extractGuards(content: string, language: string, symbols: ExtractedSymbol[]): ExtractedGuard[] {
  const guards: ExtractedGuard[] = [];
  const lines = content.split("\n");

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const lower = line.toLowerCase().trim();

    // Find enclosing symbol
    let enclosingSymbol: string | undefined;
    for (const sym of symbols) {
      if (sym.lineStart <= i + 1 && sym.lineEnd >= i + 1) {
        enclosingSymbol = sym.name;
      }
    }

    // Null checks
    if (/!= null|== null|objects\.requirenonnull|optional\.ofnullable|optional\.</i.test(line)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "null-check", evidence: line.trim(), lineNumber: i + 1 });
    }
    // Validation annotations
    if (/^@(nonnull|notnull|valid|validated|notblank|notempty|size|min|max|pattern)/i.test(lower)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "validation-annotation", evidence: line.trim(), lineNumber: i + 1 });
    }
    // Try-catch
    if (/catch\s*\(/.test(line)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "exception-handler", evidence: line.trim(), lineNumber: i + 1 });
    }
    // Parameterized SQL
    if (/preparedstatement|namedparameterjdbctemplate|@query.*:(?!:)/i.test(line)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "parameterized-sql", evidence: line.trim(), lineNumber: i + 1 });
    }
    // Auth guards
    if (/@secured|@preauthorize|@rolesallowed|@denyall/i.test(line)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "auth-guard", evidence: line.trim(), lineNumber: i + 1 });
    }
    // Input sanitization
    if (/escape|sanitize|encode|stripisolatin|htmlutils|owasp/i.test(line)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "input-sanitization", evidence: line.trim(), lineNumber: i + 1 });
    }
    // Bounds check
    if (/(\.length|\.size\(\))\s*(>|>=|<|<=|==|!=)/.test(line) || /index.*bounds|arrayindexoutofbounds/i.test(line)) {
      guards.push({ protectingSymbol: enclosingSymbol, guardType: "bounds-check", evidence: line.trim(), lineNumber: i + 1 });
    }
  }

  return guards;
}

// ─── Baseline vulnerability detection ──────────────────────────────────────────

interface ExtractedVuln {
  lineNumber: number;
  patternType: string;
  severity: string;
  fingerprint: string;
}

function detectBaselineVulns(content: string, filePath: string, language: string): ExtractedVuln[] {
  const vulns: ExtractedVuln[] = [];
  const lines = content.split("\n");

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const lower = line.toLowerCase();

    // SQL injection risk
    if (/string\.format.*select|"select.*\+|'select.*\+/i.test(line) && !/preparedstatement|namedparameter/i.test(lines.slice(Math.max(0, i - 3), i + 4).join("\n"))) {
      addVuln(vulns, filePath, i + 1, "sql-injection-risk", "error");
    }
    // Hardcoded secrets
    if (/(password|secret|api_key|apikey)\s*=\s*["'][^"']{8,}["']/i.test(line) && !/test|mock|example|placeholder/i.test(line)) {
      addVuln(vulns, filePath, i + 1, "hardcoded-secret", "critical");
    }
    // Weak crypto
    if (/md5|sha1(?!ng)|des(?!cription|tination|erialize)/i.test(lower) && /messagedigest|cipher|getinstance/i.test(lower)) {
      addVuln(vulns, filePath, i + 1, "weak-crypto", "warning");
    }
    // System.exit / os._exit
    if (/system\.exit|os\._exit|runtime\.halt/i.test(line)) {
      addVuln(vulns, filePath, i + 1, "abrupt-termination", "warning");
    }
    // Unsafe deserialization
    if (/objectinputstream|readobject|pickle\.load|unsafe.*deserializ/i.test(lower)) {
      addVuln(vulns, filePath, i + 1, "unsafe-deserialization", "error");
    }
  }

  return vulns;
}

function addVuln(vulns: ExtractedVuln[], filePath: string, lineNumber: number, patternType: string, severity: string): void {
  const lineBucket = Math.floor(lineNumber / 10) * 10;
  const fingerprint = `${filePath}|${patternType}|${lineBucket}`;
  if (!vulns.some((v) => v.fingerprint === fingerprint)) {
    vulns.push({ lineNumber, patternType, severity, fingerprint });
  }
}

// ─── AI file summary ──────────────────────────────────────────────────────────

async function aiSummarizeFile(filePath: string, content: string, symbols: ExtractedSymbol[]): Promise<{
  summary: string;
  keyResponsibilities?: string;
  inputsOutputs?: string;
  implicitContracts?: string;
} | null> {
  if (!isAIEnabled()) return null;

  const symbolList = symbols.slice(0, 20)
    .map((s) => `  ${s.type} ${s.name}${s.parentSymbol ? ` (in ${s.parentSymbol})` : ""} L${s.lineStart}`)
    .join("\n");
  const snippet = content.length > 3000 ? content.slice(0, 3000) + "\n... [truncated]" : content;

  try {
    const result = await complete({
      messages: [
        {
          role: "system",
          content: `You are a code analysis engine. Produce a brief JSON summary of the given source file.
Output ONLY a JSON object with keys: "summary" (1-2 sentences), "keyResponsibilities" (bullet points), "inputsOutputs" (main inputs/outputs), "implicitContracts" (hidden assumptions or invariants).
Be precise and technical. Do not include markdown code fences.`,
        },
        {
          role: "user",
          content: `File: ${filePath}\n\nSymbols:\n${symbolList}\n\nCode:\n${snippet}`,
        },
      ],
      temperature: 0.1,
      maxTokens: 500,
    });

    const cleaned = result.content.trim().replace(/^```json\s*/, "").replace(/```$/, "");
    const parsed = JSON.parse(cleaned);
    return {
      summary: String(parsed.summary || ""),
      keyResponsibilities: parsed.keyResponsibilities ? String(parsed.keyResponsibilities) : undefined,
      inputsOutputs: parsed.inputsOutputs ? String(parsed.inputsOutputs) : undefined,
      implicitContracts: parsed.implicitContracts ? String(parsed.implicitContracts) : undefined,
    };
  } catch {
    return null;
  }
}

// ─── File tree enumeration ─────────────────────────────────────────────────────

const BROWSE_CONCURRENCY = 8;

async function enumerateRepoFiles(repoSlug: string, basePath = "", at?: string): Promise<string[]> {
  const allFiles: string[] = [];
  let apiCalls = 0;

  async function walk(dir: string, depth: number) {
    if (depth > MAX_BROWSE_DEPTH) return;
    try {
      const entries = await browseRepo(repoSlug, dir, at);
      apiCalls++;
      if (apiCalls % 20 === 0) {
        console.log(`[RCIE] browse progress: ${apiCalls} API calls, ${allFiles.length} source files found so far`);
      }

      const subDirs: Array<{ fullPath: string; depth: number }> = [];
      for (const entry of entries) {
        const fullPath = dir ? `${dir}/${entry.path.toString}` : entry.path.toString;
        if (entry.type === "FILE") {
          if (detectLanguage(fullPath) || isConfigFile(fullPath)) {
            allFiles.push(fullPath);
          }
        } else if (entry.type === "DIRECTORY") {
          // Skip common non-source directories
          const dirName = entry.path.toString.toLowerCase();
          if (/^(node_modules|\.git|dist|build|target|__pycache__|\.idea|\.vscode|vendor|\.gradle)$/.test(dirName)) continue;
          subDirs.push({ fullPath, depth: depth + 1 });
        }
      }

      // Walk subdirectories concurrently in batches
      for (let i = 0; i < subDirs.length; i += BROWSE_CONCURRENCY) {
        const batch = subDirs.slice(i, i + BROWSE_CONCURRENCY);
        await Promise.all(batch.map((d) => walk(d.fullPath, d.depth)));
      }
    } catch {
      // Access denied or directory gone — skip
    }
  }

  console.log(`[RCIE] starting file enumeration for repo=${repoSlug} basePath="${basePath}"`);
  await walk(basePath, 0);
  console.log(`[RCIE] enumeration done: ${allFiles.length} source files found in ${apiCalls} API calls`);
  return allFiles;
}

// ─── Content hash ──────────────────────────────────────────────────────────────

function contentHash(content: string): string {
  return crypto.createHash("sha256").update(content).digest("hex").slice(0, 16);
}

// ─── Main index function ───────────────────────────────────────────────────────

export interface IndexOptions {
  repoSlug: string;
  commitSha?: string;
  basePath?: string;
  /** If true, clear existing index and re-index from scratch */
  fullReindex?: boolean;
  /** Max number of files to process per run (for incremental) */
  maxFiles?: number;
  /** Enable AI summaries for key files */
  enableAISummaries?: boolean;
}

export interface IndexResult {
  runId: string;
  status: "completed" | "failed";
  filesIndexed: number;
  symbolsExtracted: number;
  dependenciesMapped: number;
  annotationsFound: number;
  guardsDetected: number;
  aiSummariesGenerated: number;
  durationMs: number;
  error?: string;
}

export async function indexRepo(options: IndexOptions): Promise<IndexResult> {
  const {
    repoSlug, commitSha, basePath = "", fullReindex = false,
    maxFiles = 500, enableAISummaries = true,
  } = options;

  const runId = startIndexRun(repoSlug, commitSha);
  const startMs = Date.now();

  let stats = {
    filesIndexed: 0,
    symbolsExtracted: 0,
    dependenciesMapped: 0,
    annotationsFound: 0,
    guardsDetected: 0,
    aiSummariesGenerated: 0,
  };

  try {
    if (fullReindex) {
      clearRepoIndex(repoSlug);
      await clearRepoGraph(repoSlug);
    }

    // 1. Enumerate files
    const filePaths = await enumerateRepoFiles(repoSlug, basePath, commitSha);
    const filesToProcess = filePaths.slice(0, maxFiles);

    // 2. Process each file
    const codeFileInputs: UpsertCodeFileInput[] = [];
    const allSymbolInputs: { filePath: string; symbols: InsertSymbolInput[] }[] = [];
    const allDepInputs: { filePath: string; deps: InsertDependencyInput[] }[] = [];
    const allAnnotationInputs: { filePath: string; annotations: InsertAnnotationInput[] }[] = [];
    const allGuardInputs: { filePath: string; guards: InsertGuardInput[] }[] = [];
    const allVulnInputs: UpsertBaselineVulnInput[] = [];
    const allGraphNodes: GraphNodeInput[] = [];
    const allGraphEdges: GraphEdgeInput[] = [];
    const aiCandidates: Array<{ filePath: string; content: string; symbols: ExtractedSymbol[] }> = [];

    let processedCount = 0;
    for (const filePath of filesToProcess) {
      const language = detectLanguage(filePath);
      if (!language) continue;

      processedCount++;
      if (processedCount % 25 === 0) {
        console.log(`[RCIE] processing file ${processedCount}/${filesToProcess.length}: ${stats.filesIndexed} indexed, ${stats.symbolsExtracted} symbols`);
      }

      // Check if already indexed with same content hash (incremental)
      let content: string;
      try {
        content = await getFileContent(repoSlug, filePath, commitSha);
      } catch {
        continue; // File may have been deleted
      }

      const hash = contentHash(content);
      if (!fullReindex) {
        const existing = getCodeFile(repoSlug, filePath);
        if (existing && existing.contentHash === hash) continue;
      }

      const lines = content.split("\n");
      if (lines.length > MAX_FILE_LINES) continue;

      const isTest = isTestFilePath(filePath);
      const isGen = isGeneratedFile(filePath, content);
      const isInfra = isInfraConfig(filePath);

      // File record
      codeFileInputs.push({
        filePath,
        commitSha: commitSha || "HEAD",
        language,
        lineCount: lines.length,
        subsystem: classifySubsystem(filePath),
        sensitivity: detectSensitivity(filePath, content),
        isGenerated: isGen,
        isInfraConfig: isInfra,
        isTest,
        contentHash: hash,
      });

      // Clear previous index data for this file
      clearFileIndex(repoSlug, filePath);
      await clearFileGraph(repoSlug, filePath);

      // Extract symbols
      const symbols = extractSymbols(content, language, filePath);
      const symbolInputs: InsertSymbolInput[] = symbols.map((s) => ({
        filePath,
        symbolName: s.name,
        symbolType: s.type,
        visibility: s.visibility,
        isStatic: s.isStatic,
        isAbstract: s.isAbstract,
        lineStart: s.lineStart,
        lineEnd: s.lineEnd,
        parentSymbol: s.parentSymbol,
        signature: s.signature,
        returnType: s.returnType,
        parameterTypes: s.parameterTypes,
      }));
      allSymbolInputs.push({ filePath, symbols: symbolInputs });
      stats.symbolsExtracted += symbols.length;

      // Extract dependencies
      const deps = extractDependencies(content, language, filePath);
      const depInputs: InsertDependencyInput[] = deps.map((d) => ({
        sourceFile: filePath,
        sourceSymbol: d.sourceSymbol,
        targetFile: d.targetFile,
        targetSymbol: d.targetSymbol,
        dependencyType: d.dependencyType,
        isDirect: true,
        metadata: d.metadata,
      }));
      allDepInputs.push({ filePath, deps: depInputs });
      stats.dependenciesMapped += deps.length;

      // Extract annotations
      const annotations = extractAnnotations(content, language, symbols);
      const annotationInputs: InsertAnnotationInput[] = annotations.map((a) => ({
        filePath,
        targetSymbol: a.targetSymbol,
        annotationName: a.annotationName,
        annotationParams: a.annotationParams,
        classification: a.classification,
        lineNumber: a.lineNumber,
      }));
      allAnnotationInputs.push({ filePath, annotations: annotationInputs });
      stats.annotationsFound += annotations.length;

      // Extract guards
      const guards = extractGuards(content, language, symbols);
      const guardInputs: InsertGuardInput[] = guards.map((g) => ({
        filePath,
        protectingSymbol: g.protectingSymbol,
        guardType: g.guardType,
        scope: g.scope,
        evidence: g.evidence,
        lineNumber: g.lineNumber,
      }));
      allGuardInputs.push({ filePath, guards: guardInputs });
      stats.guardsDetected += guards.length;

      // Detect baseline vulnerabilities
      const vulns = detectBaselineVulns(content, filePath, language);
      for (const v of vulns) {
        allVulnInputs.push({
          filePath,
          lineNumber: v.lineNumber,
          patternType: v.patternType,
          severity: v.severity,
          fingerprint: v.fingerprint,
        });
      }

      const graph = buildGraphWarehouseForFile({
        repoSlug,
        filePath,
        content,
        symbols,
        dependencies: deps,
        annotations,
        subsystem: classifySubsystem(filePath),
      });
      allGraphNodes.push(...graph.nodes);
      allGraphEdges.push(...graph.edges);

      // AI summary candidate
      if (enableAISummaries && !isGen && !isTest && symbols.length > 0 && aiCandidates.length < MAX_AI_SUMMARY_FILES) {
        aiCandidates.push({ filePath, content, symbols });
      }

      stats.filesIndexed++;
    }

    // 2b. Process config files for bean-wiring dependencies
    const configFiles = filePaths.filter((fp) => isConfigFile(fp));
    for (const cfgPath of configFiles.slice(0, 50)) {
      let cfgContent: string;
      try {
        cfgContent = await getFileContent(repoSlug, cfgPath, commitSha);
      } catch {
        continue;
      }
      const cfgDeps = extractConfigDependencies(cfgContent, cfgPath);
      if (cfgDeps.length > 0) {
        const depInputs: InsertDependencyInput[] = cfgDeps.map((d) => ({
          sourceFile: cfgPath,
          sourceSymbol: d.sourceSymbol,
          targetFile: d.targetFile,
          targetSymbol: d.targetSymbol,
          dependencyType: d.dependencyType,
          isDirect: true,
          metadata: d.metadata,
        }));
        allDepInputs.push({ filePath: cfgPath, deps: depInputs });
        stats.dependenciesMapped += cfgDeps.length;
      }
    }

    // 3. Bulk write to DB
    upsertCodeFiles(repoSlug, codeFileInputs);
    for (const { symbols } of allSymbolInputs) insertSymbols(repoSlug, symbols);
    for (const { deps } of allDepInputs) insertDependencies(repoSlug, deps);
    for (const { annotations } of allAnnotationInputs) insertAnnotations(repoSlug, annotations);
    for (const { guards } of allGuardInputs) insertGuards(repoSlug, guards);
    upsertBaselineVulns(repoSlug, allVulnInputs);
    await upsertGraphNodes(repoSlug, allGraphNodes);
    await upsertGraphEdges(repoSlug, allGraphEdges);

    // 4. AI summaries (sequential to avoid overwhelming provider)
    if (enableAISummaries && isAIEnabled()) {
      const summaryInputs: UpsertAISummaryInput[] = [];
      const providerInfo = getProviderInfo();

      for (const candidate of aiCandidates) {
        const aiResult = await aiSummarizeFile(candidate.filePath, candidate.content, candidate.symbols);
        if (aiResult) {
          summaryInputs.push({
            filePath: candidate.filePath,
            summary: aiResult.summary,
            keyResponsibilities: aiResult.keyResponsibilities,
            inputsOutputs: aiResult.inputsOutputs,
            implicitContracts: aiResult.implicitContracts,
            contentHash: contentHash(candidate.content),
            provider: providerInfo.provider,
            model: providerInfo.model,
          });
          stats.aiSummariesGenerated++;
        }
      }

      upsertAISummaries(repoSlug, summaryInputs);
    }

    const durationMs = Date.now() - startMs;
    completeIndexRun(runId, { ...stats, durationMs });

    return {
      runId,
      status: "completed",
      ...stats,
      durationMs,
    };
  } catch (err: any) {
    const durationMs = Date.now() - startMs;
    const errorMsg = err?.message || String(err);
    failIndexRun(runId, errorMsg);
    return {
      runId,
      status: "failed",
      ...stats,
      durationMs,
      error: errorMsg,
    };
  }
}
