/** Returns true for Spring / config files that define bean wiring */
function isConfigFile(filePath: string): boolean {
  const lower = filePath.toLowerCase();
  return (
    lower.endsWith(".properties") || lower.endsWith(".yml") || lower.endsWith(".yaml") ||
    lower.endsWith(".xml")
  );
}

/**
 * Extract bean-wiring dependencies from Spring/config property files.
 * Detects datasource URLs, class references, and spring.profiles.
 */
function extractConfigDependencies(content: string, filePath: string): ExtractedDependency[] {
  const deps: ExtractedDependency[] = [];
  const lower = filePath.toLowerCase();

  if (lower.endsWith(".properties") || lower.endsWith(".yml") || lower.endsWith(".yaml")) {
    // Detect fully-qualified class references (e.g. spring.datasource.driver-class-name, spring.jpa.hibernate.ddl-auto)
    const fqcnPattern = /=\s*([a-z][a-z0-9_]*(?:\.[a-z][a-z0-9_]*){2,}(?:\.[A-Z]\w*))/g;
    let match: RegExpExecArray | null;
    while ((match = fqcnPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "config-class-ref", metadata: { configFile: filePath } });
    }
    // Detect datasource / messaging URLs
    const urlPattern = /(?:url|uri|endpoint|host)\s*[=:]\s*([^\s#]+)/gi;
    while ((match = urlPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1].slice(0, 120), dependencyType: "config-resource", metadata: { configFile: filePath } });
    }
    // Bean overrides: class: or type: references
    const beanClassPattern = /(?:class|type)\s*:\s*([a-z][a-z0-9_]*(?:\.[a-z][a-z0-9_]*)*\.[A-Z]\w*)/g;
    while ((match = beanClassPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "config-bean-ref", metadata: { configFile: filePath } });
    }
  }

  if (lower.endsWith(".xml")) {
    // Spring XML bean definitions: <bean class="com.example.MyBean"> or ref="beanName"
    const xmlBeanPattern = /class\s*=\s*"([a-z][a-z0-9_]*(?:\.[a-z][a-z0-9_]*)*\.[A-Z]\w*)"/g;
    let match: RegExpExecArray | null;
    while ((match = xmlBeanPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "config-bean-ref", metadata: { configFile: filePath } });
    }
    const xmlRefPattern = /ref\s*=\s*"([^"]+)"/g;
    while ((match = xmlRefPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "config-bean-ref", metadata: { configFile: filePath } });
    }
  }

  return deps;
}

function isInfraConfig(filePath: string): boolean {
  const name = filePath.toLowerCase();
  return /(^|\/)(dockerfile|docker-compose|helm|charts|terraform|k8s|kubernetes|deploy|deployment|jenkins|github\/workflows|cloudformation|ansible|pulumi|infra|config)\b/.test(name)
    || /\.(ya?ml|json|toml|ini|properties|conf|env)$/.test(name);
}

function detectMicroserviceName(filePath: string, repoSlug: string): string {
  const normalized = filePath.replace(/^\/+/, "");
  const parts = normalized.split("/").filter(Boolean);
  if (parts.length === 0) return repoSlug;
  const first = parts[0].toLowerCase();
  if (["src", "lib", "app", "test", "tests", "config"].includes(first)) return repoSlug;
  return parts[0];
}

function buildMethodBody(content: string, lineStart?: number, lineEnd?: number): string | undefined {
  if (!lineStart || !lineEnd || lineStart < 1 || lineEnd < lineStart) return undefined;
  const lines = content.split("\n");
  const body = lines.slice(lineStart - 1, Math.min(lineEnd, lines.length)).join("\n");
  return body.length > 12000 ? body.slice(0, 12000) : body;
}

function buildGraphWarehouseForFile(params: {
  repoSlug: string;
  filePath: string;
  content: string;
  symbols: ExtractedSymbol[];
  dependencies: ExtractedDependency[];
  annotations: ExtractedAnnotation[];
  subsystem: string;
}): { nodes: GraphNodeInput[]; edges: GraphEdgeInput[] } {
  const { repoSlug, filePath, content, symbols, dependencies, annotations, subsystem } = params;

  const nodes: GraphNodeInput[] = [];
  const edges: GraphEdgeInput[] = [];
  const nodeSeen = new Set<string>();
  const edgeSeen = new Set<string>();
  const symbolToNode = new Map<string, string>();

  const sourceMicroservice = detectMicroserviceName(filePath, repoSlug);
  const fileNodeKey = `file:${filePath}`;
  const microserviceNodeKey = `microservice:${sourceMicroservice}`;

  const pushNode = (node: GraphNodeInput) => {
    const key = `${node.nodeType}:${node.nodeKey}`;
    if (nodeSeen.has(key)) return;
    nodeSeen.add(key);
    nodes.push(node);
  };

  const pushEdge = (edge: GraphEdgeInput) => {
    const key = `${edge.fromNodeKey}->${edge.toNodeKey}:${edge.edgeType}`;
    if (edgeSeen.has(key)) return;
    edgeSeen.add(key);
    edges.push(edge);
  };

  pushNode({
    nodeKey: fileNodeKey,
    nodeType: "File",
    filePath,
    microservice: sourceMicroservice,
    metadata: { subsystem },
  });

  pushNode({
    nodeKey: microserviceNodeKey,
    nodeType: "Microservice",
    microservice: sourceMicroservice,
  });

  pushEdge({ fromNodeKey: fileNodeKey, toNodeKey: microserviceNodeKey, edgeType: "MICROSERVICE_LINK" });

  for (const symbol of symbols) {
    const symbolNodeKey = `symbol:${filePath}:${symbol.name}:${symbol.lineStart || 0}`;
    symbolToNode.set(symbol.name, symbolNodeKey);

    const symbolType: GraphNodeInput["nodeType"] =
      symbol.type === "class" || symbol.type === "interface" || symbol.type === "enum" || symbol.type === "struct"
        ? "Class"
        : "Method";

    const symbolAnnotations = annotations
      .filter((a) => a.targetSymbol === symbol.name)
      .map((a) => a.annotationName);

    pushNode({
      nodeKey: symbolNodeKey,
      nodeType: symbolType,
      filePath,
      symbolName: symbol.name,
      microservice: sourceMicroservice,
      signature: symbol.signature,
      visibility: symbol.visibility,
      returnType: symbol.returnType,
      lineStart: symbol.lineStart,
      lineEnd: symbol.lineEnd,
      methodBody: symbolType === "Method" ? buildMethodBody(content, symbol.lineStart, symbol.lineEnd) : undefined,
      annotations: symbolAnnotations,
      metadata: { parentSymbol: symbol.parentSymbol || null },
    });

    pushEdge({ fromNodeKey: fileNodeKey, toNodeKey: symbolNodeKey, edgeType: "USES" });
  }

  for (const annotation of annotations) {
    const annotationNodeKey = `annotation:${annotation.annotationName.toLowerCase()}`;
    pushNode({
      nodeKey: annotationNodeKey,
      nodeType: "Annotation",
      filePath,
      symbolName: annotation.annotationName,
      metadata: {
        classification: annotation.classification,
        lineNumber: annotation.lineNumber,
      },
    });

    const targetKey = symbolToNode.get(annotation.targetSymbol);
    if (targetKey) {
      pushEdge({ fromNodeKey: targetKey, toNodeKey: annotationNodeKey, edgeType: "HAS_ANNOTATION" });
    }
  }

  for (const dep of dependencies) {
    const sourceNodeKey = dep.sourceSymbol && symbolToNode.get(dep.sourceSymbol)
      ? symbolToNode.get(dep.sourceSymbol)!
      : fileNodeKey;

    let targetNodeKey: string;
    if (dep.targetFile) {
      targetNodeKey = `file:${dep.targetFile}`;
      pushNode({
        nodeKey: targetNodeKey,
        nodeType: "File",
        filePath: dep.targetFile,
        microservice: detectMicroserviceName(dep.targetFile, repoSlug),
      });
    } else if (dep.targetSymbol) {
      const existing = symbolToNode.get(dep.targetSymbol);
      if (existing) {
        targetNodeKey = existing;
      } else {
        const guessedType: GraphNodeInput["nodeType"] =
          ["extends", "implements", "injection"].includes(dep.dependencyType)
            ? "Class"
            : "Method";
        targetNodeKey = `external:${dep.targetSymbol}`;
        pushNode({
          nodeKey: targetNodeKey,
          nodeType: guessedType,
          symbolName: dep.targetSymbol,
        });
      }
    } else {
      continue;
    }

    pushEdge({
      fromNodeKey: sourceNodeKey,
      toNodeKey: targetNodeKey,
      edgeType: dep.dependencyType === "method-call" ? "CALLS" : "USES",
      metadata: { dependencyType: dep.dependencyType },
    });

    if (dep.targetFile) {
      const targetMicroservice = detectMicroserviceName(dep.targetFile, repoSlug);
      if (targetMicroservice && targetMicroservice !== sourceMicroservice) {
        const targetServiceNodeKey = `microservice:${targetMicroservice}`;
        pushNode({
          nodeKey: targetServiceNodeKey,
          nodeType: "Microservice",
          microservice: targetMicroservice,
        });
        pushEdge({
          fromNodeKey: microserviceNodeKey,
          toNodeKey: targetServiceNodeKey,
          edgeType: "DEPENDS_ON_SERVICE",
        });
      }
    }
  }

  return { nodes, edges };
}

// ─── Symbol extraction ─────────────────────────────────────────────────────────

interface ExtractedSymbol {
  name: string;
  type: "class" | "interface" | "enum" | "function" | "method" | "constant" | "struct";
  visibility?: string;
  isStatic: boolean;
  isAbstract: boolean;
  lineStart: number;
  lineEnd: number;
  parentSymbol?: string;
  signature?: string;
  returnType?: string;
  parameterTypes?: string;
}

function extractSymbols(content: string, language: string, filePath: string): ExtractedSymbol[] {
  const lines = content.split("\n");
  const symbols: ExtractedSymbol[] = [];

  if (language === "java" || language === "groovy") {
    extractJavaSymbols(lines, symbols);
  } else if (language === "typescript" || language === "javascript") {
    extractTsSymbols(lines, symbols);
  } else if (language === "python") {
    extractPythonSymbols(lines, symbols);
  } else if (language === "c/c++") {
    extractCppSymbols(lines, symbols);
  }

  // Compute line end via brace/indent tracking (simplified)
  for (let i = 0; i < symbols.length; i++) {
    const nextStart = symbols[i + 1]?.lineStart ?? lines.length + 1;
    if (symbols[i].lineEnd <= symbols[i].lineStart) {
      symbols[i].lineEnd = nextStart - 1;
    }
  }

  return symbols;
}

function extractJavaSymbols(lines: string[], symbols: ExtractedSymbol[]): void {
  const classPattern = /^\s*(public|private|protected|abstract|final|static)?\s*(class|interface|enum)\s+([A-Za-z_]\w*)/;
  const methodPattern = /^\s*(public|private|protected|static|final|abstract|synchronized|\s)*\s*(<[^>]+>\s+)?([A-Za-z_]\w*(?:<[^>]*>)?(?:\[\])?)\s+([A-Za-z_]\w*)\s*\(([^)]*)\)/;
  let currentClass: string | undefined;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const classMatch = line.match(classPattern);
    if (classMatch) {
      currentClass = classMatch[3];
      symbols.push({
        name: currentClass,
        type: classMatch[2] as "class" | "interface" | "enum",
        visibility: classMatch[1] || "package-private",
        isStatic: line.includes("static"),
        isAbstract: line.includes("abstract"),
        lineStart: i + 1,
        lineEnd: i + 1,
        signature: line.trim(),
      });
      continue;
    }
    const methodMatch = line.match(methodPattern);
    if (methodMatch && !line.trim().startsWith("//") && !line.trim().startsWith("*")) {
      const visibility = /public|private|protected/.exec(line)?.[0] || "package-private";
      symbols.push({
        name: methodMatch[4],
        type: "method",
        visibility,
        isStatic: line.includes("static"),
        isAbstract: line.includes("abstract"),
        lineStart: i + 1,
        lineEnd: i + 1,
        parentSymbol: currentClass,
        signature: line.trim(),
        returnType: methodMatch[3],
        parameterTypes: methodMatch[5]?.trim() || undefined,
      });
    }
  }
}

function extractTsSymbols(lines: string[], symbols: ExtractedSymbol[]): void {
  const classPattern = /^\s*(export\s+)?(abstract\s+)?class\s+([A-Za-z_]\w*)/;
  const funcPattern = /^\s*(export\s+)?(async\s+)?function\s+([A-Za-z_]\w*)/;
  const constPattern = /^\s*(export\s+)?const\s+([A-Za-z_]\w*)\s*=\s*(async\s+)?\(/;
  const methodPattern = /^\s*(public|private|protected|static|async|\s)*\s*([A-Za-z_]\w*)\s*\(([^)]*)\)\s*[:{]/;

  let currentClass: string | undefined;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const classMatch = line.match(classPattern);
    if (classMatch) {
      currentClass = classMatch[3];
      symbols.push({
        name: currentClass,
        type: "class",
        visibility: line.includes("export") ? "public" : "private",
        isStatic: false,
        isAbstract: line.includes("abstract"),
        lineStart: i + 1,
        lineEnd: i + 1,
        signature: line.trim(),
      });
      continue;
    }
    const funcMatch = line.match(funcPattern);
    if (funcMatch) {
      symbols.push({
        name: funcMatch[3],
        type: "function",
        visibility: line.includes("export") ? "public" : "private",
        isStatic: false,
        isAbstract: false,
        lineStart: i + 1,
        lineEnd: i + 1,
        signature: line.trim(),
      });
      continue;
    }
    const constMatch = line.match(constPattern);
    if (constMatch) {
      symbols.push({
        name: constMatch[2],
        type: "function",
        visibility: line.includes("export") ? "public" : "private",
        isStatic: false,
        isAbstract: false,
        lineStart: i + 1,
        lineEnd: i + 1,
        signature: line.trim(),
      });
      continue;
    }
    if (currentClass) {
      const methodMatch = line.match(methodPattern);
      if (methodMatch && !line.trim().startsWith("//") && !line.trim().startsWith("*")) {
        symbols.push({
          name: methodMatch[2],
          type: "method",
          visibility: /public|private|protected/.exec(line)?.[0] || "public",
          isStatic: line.includes("static"),
          isAbstract: false,
          lineStart: i + 1,
          lineEnd: i + 1,
          parentSymbol: currentClass,
          signature: line.trim(),
          parameterTypes: methodMatch[3]?.trim() || undefined,
        });
      }
    }
  }
}

function extractPythonSymbols(lines: string[], symbols: ExtractedSymbol[]): void {
  const classPattern = /^class\s+([A-Za-z_]\w*)\s*[:(]/;
  const funcPattern = /^(\s*)(async\s+)?def\s+([A-Za-z_]\w*)\s*\(([^)]*)\)/;
  let currentClass: string | undefined;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const classMatch = line.match(classPattern);
    if (classMatch) {
      currentClass = classMatch[1];
      symbols.push({
        name: currentClass,
        type: "class",
        visibility: currentClass.startsWith("_") ? "private" : "public",
        isStatic: false,
        isAbstract: false,
        lineStart: i + 1,
        lineEnd: i + 1,
        signature: line.trim(),
      });
      continue;
    }
    const funcMatch = line.match(funcPattern);
    if (funcMatch) {
      const indent = funcMatch[1].length;
      const name = funcMatch[3];
      const isMethod = indent > 0 && currentClass;
      symbols.push({
        name,
        type: isMethod ? "method" : "function",
        visibility: name.startsWith("_") ? "private" : "public",
        isStatic: false,
        isAbstract: false,
        lineStart: i + 1,
        lineEnd: i + 1,
        parentSymbol: isMethod ? currentClass : undefined,
        signature: line.trim(),
        parameterTypes: funcMatch[4]?.trim() || undefined,
      });
    }
  }
}

function extractCppSymbols(lines: string[], symbols: ExtractedSymbol[]): void {
  const classPattern = /^\s*(class|struct)\s+([A-Za-z_]\w*)/;
  const funcPattern = /^\s*(static\s+|inline\s+|constexpr\s+|virtual\s+)?([A-Za-z_][\w:<>~*&]*)\s+([A-Za-z_]\w*)\s*\(([^)]*)\)/;
  let currentClass: string | undefined;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const classMatch = line.match(classPattern);
    if (classMatch) {
      currentClass = classMatch[2];
      symbols.push({
        name: currentClass,
        type: classMatch[1] === "struct" ? "struct" : "class",
        visibility: "public",
        isStatic: false,
        isAbstract: false,
        lineStart: i + 1,
        lineEnd: i + 1,
        signature: line.trim(),
      });
      continue;
    }
    const funcMatch = line.match(funcPattern);
    if (funcMatch && !line.trim().startsWith("//") && !line.trim().startsWith("*") && !line.trim().startsWith("#")) {
      symbols.push({
        name: funcMatch[3],
        type: currentClass ? "method" : "function",
        visibility: "public",
        isStatic: line.includes("static"),
        isAbstract: line.includes("virtual") && line.includes("= 0"),
        lineStart: i + 1,
        lineEnd: i + 1,
        parentSymbol: currentClass,
        signature: line.trim(),
        returnType: funcMatch[2],
        parameterTypes: funcMatch[4]?.trim() || undefined,
      });
    }
  }
}

// ─── Dependency extraction ─────────────────────────────────────────────────────

interface ExtractedDependency {
  sourceSymbol?: string;
  targetFile?: string;
  targetSymbol?: string;
  dependencyType: string;
  metadata?: Record<string, unknown>;
}

function extractDependencies(content: string, language: string, filePath: string): ExtractedDependency[] {
  const deps: ExtractedDependency[] = [];

  if (language === "java" || language === "groovy") {
    // Import dependencies
    const importPattern = /^import\s+(?:static\s+)?([a-zA-Z_][\w.]*);/gm;
    let match: RegExpExecArray | null;
    while ((match = importPattern.exec(content)) !== null) {
      const fqn = match[1];
      const isStatic = match[0].includes("static");
      deps.push({
        targetSymbol: fqn,
        dependencyType: isStatic ? "static-import" : "import",
      });
    }
    // Inheritance
    const extendsPattern = /class\s+\w+\s+extends\s+([A-Za-z_]\w*)/g;
    while ((match = extendsPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "extends" });
    }
    const implPattern = /class\s+\w+.*?implements\s+([A-Za-z_][\w,\s<>]*)/g;
    while ((match = implPattern.exec(content)) !== null) {
      const impls = match[1].split(",").map((s) => s.trim().replace(/<.*>/, ""));
      for (const impl of impls) {
        if (impl) deps.push({ targetSymbol: impl, dependencyType: "implements" });
      }
    }
    // Spring injection
    const injectPattern = /@(?:Autowired|Inject|Resource)\s+(?:private\s+)?([A-Za-z_]\w*)\s+(\w+)/g;
    while ((match = injectPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "injection" });
    }
    // Method calls to other services (heuristic: camelCase field calls)
    const callPattern = /(?:this\.)?([a-z]\w*(?:Service|Repository|Dao|Client|Handler|Manager))\.\w+\(/g;
    while ((match = callPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[1], dependencyType: "method-call" });
    }
    // AOP cross-cutting: @Around / @Before / @After with pointcut targeting other classes
    const aopPattern = /@(?:Around|Before|After|AfterReturning|AfterThrowing)\s*\(\s*["']([^"']+)["']/g;
    while ((match = aopPattern.exec(content)) !== null) {
      const pointcut = match[1];
      // Extract target class/method from pointcut expressions like "execution(* com.example.MyService.*(..))" 
      const targetMatch = pointcut.match(/execution\([^)]*?([A-Z]\w+)\./);
      deps.push({
        targetSymbol: targetMatch ? targetMatch[1] : pointcut.slice(0, 80),
        dependencyType: "aop-advice",
        metadata: { pointcut },
      });
    }
    // Lombok @RequiredArgsConstructor implicit injection (final fields become constructor-injected)
    if (/@RequiredArgsConstructor/i.test(content)) {
      const finalFieldPattern = /private\s+final\s+([A-Z]\w+)\s+(\w+)\s*;/g;
      while ((match = finalFieldPattern.exec(content)) !== null) {
        deps.push({ targetSymbol: match[1], dependencyType: "injection", metadata: { via: "lombok-constructor" } });
      }
    }
  } else if (language === "typescript" || language === "javascript") {
    const tsImportPattern = /import\s+(?:{([^}]+)}|(\w+))\s+from\s+['"]([^'"]+)['"]/g;
    let match: RegExpExecArray | null;
    while ((match = tsImportPattern.exec(content)) !== null) {
      const specifiers = match[1] || match[2] || "";
      const source = match[3];
      deps.push({
        targetFile: source,
        targetSymbol: specifiers.trim(),
        dependencyType: source.startsWith(".") ? "local-import" : "package-import",
      });
    }
    // require() calls
    const requirePattern = /require\(\s*['"]([^'"]+)['"]\s*\)/g;
    while ((match = requirePattern.exec(content)) !== null) {
      deps.push({
        targetFile: match[1],
        dependencyType: match[1].startsWith(".") ? "local-import" : "package-import",
      });
    }
  } else if (language === "python") {
    const pyImportPattern = /^(?:from\s+(\S+)\s+)?import\s+(.+)/gm;
    let match: RegExpExecArray | null;
    while ((match = pyImportPattern.exec(content)) !== null) {
      const module = match[1] || match[2].split(",")[0].trim();
      deps.push({
        targetSymbol: module,
        targetFile: match[1] || undefined,
        dependencyType: module.startsWith(".") ? "relative-import" : "import",
      });
    }
    // Inheritance
    const pyClassPattern = /class\s+\w+\(([^)]+)\)/g;
    while ((match = pyClassPattern.exec(content)) !== null) {
      const bases = match[1].split(",").map((s) => s.trim());
      for (const base of bases) {
        if (base && base !== "object") {
          deps.push({ targetSymbol: base, dependencyType: "extends" });
        }
      }
    }
  } else if (language === "c/c++") {
    const includePattern = /^#include\s+[<"]([^>"]+)[>"]/gm;
    let match: RegExpExecArray | null;
    while ((match = includePattern.exec(content)) !== null) {
      deps.push({
        targetFile: match[1],
        dependencyType: match[0].includes("<") ? "system-include" : "local-include",
      });
    }
    // Inheritance
    const cppClassPattern = /class\s+\w+\s*:\s*(public|private|protected)\s+([A-Za-z_]\w*)/g;
    while ((match = cppClassPattern.exec(content)) !== null) {
      deps.push({ targetSymbol: match[2], dependencyType: "extends" });
    }
  }

  return deps;
}

